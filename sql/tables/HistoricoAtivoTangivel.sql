CREATE TABLE historicoativotangivel (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY,
    id_ativo_tangivel BIGINT,
    id_nota_fiscal BIGINT,
    id_ativo BIGINT,
    nome_ativo VARCHAR(100),
    custo_aquisicao_ativo NUMERIC(10, 2),
    tipo_ativo VARCHAR(20),
    tag_ativo VARCHAR(20),
    grau_importancia_ativo BIGINT,
    status_ativo VARCHAR(50),
    descricao_ativo VARCHAR(500),
    numero_identificacao_ativo VARCHAR(50),
    ultima_atualizacao_ativo DATE,
    marca_ativo VARCHAR(100),
    data_aquisicao_ativo DATE,
    campos_personalizados_ativo TEXT,
    garantia__ativo_tangivel DATE,
    taxa_depreciacao_ativo_tangivel NUMERIC(10, 2),
    periodo_depreciacao_ativo_tangivel VARCHAR(30),
    id_usuario BIGINT,
    nome_usuario VARCHAR(100),
    cpf_usuario VARCHAR(11),
    nascimento_usuario DATE,
    departamento_usuario VARCHAR(20),
    telefone_usuario VARCHAR(20),
    email_usuario VARCHAR(100),
    status_usuario VARCHAR(100),
    documento_nota_fiscal BYTEA,
    tipo_documento_nota_fiscal VARCHAR(30),
    data_cadastro_ativo DATE,
    valor_residual NUMERIC,
    CONSTRAINT HistoricoAtivoTangivel_pkey PRIMARY KEY (id),
    CONSTRAINT fk_historicoativotangivel_id_nota_fiscal FOREIGN KEY (id_nota_fiscal) REFERENCES notafiscal (id)
);
